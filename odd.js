
function isOdd(num) {
	if (num % 2 == 0)
		return false;
	else
		return true;
}


console.log("3 is odd: " + isOdd(3));
console.log("8 is odd: " + isOdd(8));
console.log("13 is odd: " + isOdd(13));
console.log("8423 is odd: " + isOdd(8423));
console.log("2343 is odd: " + isOdd(2343));
console.log("8654 is odd: " + isOdd(8654));
